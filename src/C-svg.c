      fprintf(draw_fp," orient=\"auto\">\n");
      fprintf(draw_fp," <path d=\"M 0 0 L 10 5 L 0 10 z\" />\n");
      fprintf(draw_fp,"</marker>\n");

   fprintf(draw_fp,"</defs>\n");

   fprintf(draw_fp,"<!-- id=\"Page%d\" -->\n", pgroup);

   vdevice.depth = 8;
