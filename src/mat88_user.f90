subroutine mat88_user(a,m,n,s,t)  ! sample mat88_user routine
implicit none
integer,intent(in)         :: m,n
doubleprecision            :: a(m,n)
doubleprecision,intent(in) :: s,t

integer                    :: i10, i20, i30, i40
!
   if(s.eq.0.and.t.eq.0)then
      ! print statistics for matrix, for example
      write(*,*)'M=',m
      write(*,*)'N=',n
      write(*,*)'S=',s
      write(*,*)'T=',t
      do i10 = 1, m
         do i20 = 1, n
            write(*,*)i10,i20,a(i10,i20)
         enddo
      enddo
   else
      do i30 = 1, m
         do i40 = 1, n
            a(i30,i40)=a(i30,i40)*s+t
         enddo
      enddo
   endif
end subroutine mat88_user
