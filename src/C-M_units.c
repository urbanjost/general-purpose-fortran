#include <math.h>
float d2r(float degrees) {
#ident "@(#)d2r(3c): Convert degrees to radians"
   return((degrees)*acos(-1.0)/180.0);
}
/*================================================================================================================================*/
#include <math.h>
float r2d(float radians) {
#ident "@(#)r2d(3c): Convert radians to degrees"
   return((radians)*180.0/acos(-1.0));
}
